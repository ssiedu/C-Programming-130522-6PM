#include<stdio.h>
#include<math.h>
void main()
{
	printf("\n ceil function : %.2f",ceil(3.2));
	printf("\n ceil function : %.2f",ceil(3.8));
	
	printf("\n floor function : %.2f",floor(3.2));
	printf("\n floor function : %.2f",floor(3.8));
	
	printf("\n sqrt function : %.2f",sqrt(16));
	printf("\n sqrt function : %.2f",sqrt(7));
	
	printf("\n pow function : %.2f",pow(3,2));
	printf("\n pow function : %.2f",pow(3.8,2));
	
	printf("\n abs function : %d",abs(-15));
	getch();
	
}
